    private void jButtoncontarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtoncontarActionPerformed

        int posicion = 0;
        int palabras = 0;
        int numdeletras = 0;
        final String ESPACIO = " ";

        String cadena = jTextAreatexto.getText();
        numdeletras = jTextAreatexto.getText().length();
        
        if (numdeletras > 0) {
            if (!(String.valueOf(cadena.charAt(numdeletras)).equals(ESPACIO))) {
                cadena += ESPACIO;
            }
        }
        if (numdeletras > 1) {
            do {
                if (String.valueOf(cadena.charAt(posicion))
                        .equals(ESPACIO)) {
                    if ((String.valueOf(cadena.charAt(posicion))
                            .equals(String.valueOf(cadena.charAt(posicion+1))))) {
                        posicion++;
                    }else{
                        palabras++;
                    }
                    posicion++;
                } else {
                    posicion++;
                }
            } while (posicion < numdeletras);
            if (numdeletras > 1) {
                palabras = palabras + 1;
            }
            jLabelnumeropalabras.setText("El n√∫mero de palabras es: " + palabras);
        } else {
            jLabelnumeropalabras.setText("Introduce un Texto. ");
        }
    }//GEN-LAST:event_jButtoncontarActionPerformed

